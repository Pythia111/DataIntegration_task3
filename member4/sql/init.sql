-- ============================================================
-- 学院C (MySQL) 数据库初始化脚本
-- 包含：建库、建表、50名学生、10门课程、250条选课记录、10个账户
-- ============================================================

CREATE DATABASE IF NOT EXISTS CollegeC DEFAULT CHARSET utf8mb4 COLLATE utf8mb4_general_ci;
USE CollegeC;

-- 账户表
DROP TABLE IF EXISTS AccountC;
CREATE TABLE AccountC (
    acc        VARCHAR(12)  PRIMARY KEY,
    passwd     VARCHAR(12)  NOT NULL,
    CreateDate TIMESTAMP    DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 学生表
DROP TABLE IF EXISTS StudentC;
CREATE TABLE StudentC (
    Sno  VARCHAR(9)  PRIMARY KEY,
    Snm  VARCHAR(10) NOT NULL,
    Sex  VARCHAR(1)  NOT NULL,
    Sde  VARCHAR(6)  NOT NULL,
    Pwd  CHAR(6)     NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 课程表
DROP TABLE IF EXISTS CourseC;
CREATE TABLE CourseC (
    Cno   CHAR(4)      PRIMARY KEY,
    Cnm   VARCHAR(10)  NOT NULL,
    Ctm   INT          NOT NULL,
    Cpt   INT          NOT NULL,
    Tec   VARCHAR(20)  NOT NULL,
    Pla   VARCHAR(18)  NOT NULL,
    Share CHAR(1)      DEFAULT 'N'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 选课表
DROP TABLE IF EXISTS CourseChoiceC;
CREATE TABLE CourseChoiceC (
    Cno  CHAR(4)    NOT NULL,
    Sno  CHAR(9)    NOT NULL,
    Grd  INT        DEFAULT 0,
    PRIMARY KEY (Cno, Sno),
    FOREIGN KEY (Cno) REFERENCES CourseC(Cno),
    FOREIGN KEY (Sno) REFERENCES StudentC(Sno)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

